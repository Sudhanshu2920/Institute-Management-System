    private String GPcalculator(String GP,String credit){
        if(GP.equals("AA"))
            return Integer.toString(10*Integer.parseInt(credit));
        else if(GP.equals("AB"))
            return Integer.toString(9*Integer.parseInt(credit));
        else if(GP.equals("BB"))
            return Integer.toString(8*Integer.parseInt(credit));
        else if(GP.equals("BC"))
            return Integer.toString(7*Integer.parseInt(credit));
        else if(GP.equals("CC"))
            return Integer.toString(6*Integer.parseInt(credit));
        else if(GP.equals("CD"))
            return Integer.toString(5*Integer.parseInt(credit));
        else if(GP.equals("NA"))
            return "NA";
        else
            return "0";
    }
    
    

