    private void updateTable(){
        if(sem.getSelectedIndex()>0){
        try{
           //DefaultTableModel model = new DefaultTableModel();
            //model.setColumnIdentifiers(columnNames);
            //tableAttendence.setModel(model);
            DefaultTableModel dtm = (DefaultTableModel) tableResult.getModel();
            int rowCount = dtm.getRowCount();
//Remove rows one by one from the end of the table
           for (int i = rowCount - 1; i >= 0; i--) {
             dtm.removeRow(i);
               }
            String sql="select Course_id,Title,credits,grade from (select course_id,grade from takes where stu_id=\""+studentId+"\" and semester="+sem.getSelectedItem().toString()+") as dummy natural join course";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery(sql);

            while(rs.next())
            {
                dtm.addRow(new String[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),GPcalculator(rs.getString(4),rs.getString(3))});
            }
            rowCount=dtm.getRowCount();
            int totalcred=0;
            int totalgp=0;
            for(int i=0;i<rowCount;i++){
                totalcred=totalcred+Integer.parseInt(dtm.getValueAt(i,2).toString());
                totalgp=totalgp+Integer.parseInt(dtm.getValueAt(i,4).toString());
            }
            float sp=(float)totalgp/totalcred;
            spi.setText(Float.toString(sp));
            //JOptionPane.showMessageDialog(null,Integer.toString(totalcred)+Integer.toString(totalgp));
            

            //tableAttendence.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(java.lang.NumberFormatException w){
            JOptionPane.showMessageDialog(null,"Total result has not been uploaded yet");
            spi.setText("NA");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
            
        }}
    }
