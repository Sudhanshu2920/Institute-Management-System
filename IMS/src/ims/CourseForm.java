    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int p=JOptionPane.showConfirmDialog(null, "Are you Sure to Save?","Save",JOptionPane.YES_NO_OPTION);
        if(p==0){
            try{
                String sql="insert into course(course_id,title,credits,department)values(?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1,courseId.getText());
            pst.setString(2,title.getText());
            pst.setString(3,courseCredit.getText());
            pst.setString(4,department.getSelectedItem().toString());
            pst.execute();
            if(!pre_required_course.getSelectedItem().toString().equals("None")){
                
                sql="insert into Prereq(Course_id,Prereq_id) values(?,?)";
                pst=conn.prepareStatement(sql);
                pst.setString(1,courseId.getText());
                pst.setString(2,pre_required_course.getSelectedItem().toString());
                pst.execute();
            }
            JOptionPane.showMessageDialog(null, "Data Save Successfully.");
                        reset_all();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

