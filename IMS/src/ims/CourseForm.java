    private void preRequiredCourse()
    {
        try
        {
            String sql="select Course_id from Course";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery(sql);
            pre_required_course.addItem("None");
            while(rs.next())
            {
                pre_required_course.addItem(rs.getString(1));
            }
            
        }
        catch(Exception e)
        {
            
        }
    }
    
    private void departmentItem()
    {
        try
        {
            String sql="select dept_name from department";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery(sql);
            
            while(rs.next())
            {
                department.addItem(rs.getString(1));
            }
            
        }
        catch(Exception e)
        {
            
        }
        
        
        
    }
